function somarNumeros(numero1, numero2){
    return numero1 + numero2;
}

function subtrairNumeros(ANumero1, ANumero2){
    return ANumero1 - ANumero2;
}

module.exports = {
    somarNumeros, 
    subtrairNumeros
};

